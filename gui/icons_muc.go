func getMUCIcon(name string) Icon {
	ico, ok := mucIcon[name]
	if !ok {
		panic(fmt.Sprintf("getMUCIcon(): icon %s doesn't exists", name))
	}
	return ico
}

func getMUCIconPixbuf(name string) gdki.Pixbuf {
	ico := getMUCIcon(name)
	return ico.GetPixbuf()
}
